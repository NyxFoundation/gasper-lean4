// static/render-plugins.js
// Verso literate 出力の <pre> 内 %%name マーカーを検出し、
// 対応する描画ライブラリで置換する。
//
// ── 仕組み（literate genre）──
// docstring 内のフェンスドコードブロック先頭行を %%name にすると、
//   Verso は <pre>（…%%name\ncontents…）を出力する。
// JS 側: このスクリプトが <pre> を走査し、textContent 先頭の %%name を
//   検出して、対応するプラグインの描画結果で置換する。
// （※ @[code_block name] による Block.code 注入は Manual genre 用で、
//    本プロジェクト（literate のみ）では使わない。
//    現状プロジェクト内に %% マーカーは無く、本スクリプトは将来の図表用。）
//
// ── 設計方針 ──
// 1. <pre> の textContent 先頭 2 文字 "%%" のみをガードに使い、
//    大半の <pre>（コードブロック）を O(1) でスキップする。
// 2. CDN スクリプトは初回検出時に遅延読み込みし、
//    読み込み完了まで対象要素に plugin-loading クラスを付与する。
// 3. MutationObserver は tippy 等の動的挿入に対応するが、
//    data-plugin-done 属性で二重処理を防止する。
// 4. cdnPlugin ファクトリで構造化し、新プラグインの追加を
//    設定オブジェクト一つに集約する。
(function () {
  "use strict";

  // ── CDN プラグインファクトリ ────────────────────────────
  // 冪等ロード: 二回目以降は即座にコールバックを呼ぶ。
  // 読み込み中に複数要素が検出された場合は queue に蓄積し、
  // onload で全部発火する。
  function cdnPlugin(opts) {
    var loaded = false;
    var queue = [];
    return {
      load: function (cb) {
        if (loaded) {
          cb();
          return;
        }
        queue.push(cb);
        if (queue.length > 1) return;
        // configure: バンドル読込み「前」に走らせる設定（例: MathJax は
        // window.MathJax を読込み前に置く必要がある）。init は読込み「後」。
        if (opts.configure) opts.configure();
        var s = document.createElement("script");
        s.src = opts.src;
        s.onload = function () {
          if (opts.init) opts.init();
          loaded = true;
          for (var i = 0; i < queue.length; i++) queue[i]();
          queue = [];
        };
        s.onerror = function () {
          console.error("Failed to load plugin script: " + opts.src);
          for (var i = 0; i < queue.length; i++) queue[i]();
          queue = [];
        };
        document.head.appendChild(s);
      },
      render: opts.render,
    };
  }

  // ── プラグイン定義 ──────────────────────────────────────
  var plugins = {
    // mermaid: 公式推奨の「ESM 両輪」構成で読み込む。本体(mermaid.esm)と ELK
    // レイアウトエンジン(layout-elk.esm・非同梱)を同一 ESM インスタンスとして
    // import し、registerLayoutLoaders → initialize する。UMD 本体へ ESM の ELK を
    // 後付けすると、ELK 側が参照する mermaid と window.mermaid が別インスタンスに
    // なり得て脆いので、両方 ESM で揃える(公式 CDN 例と一致)。両 import が解決
    // してから render を解放するので、どの描画も ELK 登録より先には走らない。ELK は
    // swimlane/flowchart の入れ子 subgraph を compound-graph として配置でき、
    // `config: layout: elk` を宣言した図(入れ子 swimlane の explode)で使う。
    // cdnPlugin を使わないのは、この import→登録→解放の順序制御のため。
    mermaid: (function () {
      var loaded = false;
      var queue = [];
      var mermaidApi = null;
      function flush() {
        loaded = true;
        for (var i = 0; i < queue.length; i++) queue[i]();
        queue = [];
      }
      function render(el, content) {
        var div = document.createElement("div");
        div.className = "mermaid";
        div.textContent = content;
        el.parentNode.replaceChild(div, el);
        if (!mermaidApi) return; // 読込失敗時は素の <div> を残し他処理は継続
        var p = mermaidApi.run({ nodes: [div] });
        // 描画完了後: 識別子ラベルへ Verso のホバー/定義リンクを橋渡し
        // (diagram-hover.js)、さらに zoom/pan/全画面ビューへ包む(diagram-view.js)。
        if (p && p.then) {
          p.then(function () {
            if (window.LeanDiagramHover) window.LeanDiagramHover.enhance(div);
            if (window.DiagramView) window.DiagramView.attach(div);
          });
        }
      }
      return {
        load: function (cb) {
          if (loaded) {
            cb();
            return;
          }
          queue.push(cb);
          if (queue.length > 1) return;
          Promise.all([
            import("https://cdn.jsdelivr.net/npm/mermaid@11/dist/mermaid.esm.min.mjs"),
            import("https://cdn.jsdelivr.net/npm/@mermaid-js/layout-elk@0/dist/mermaid-layout-elk.esm.min.mjs"),
          ])
            .then(function (mods) {
              var mermaid = mods[0].default || mods[0];
              var elk = mods[1].default || mods[1];
              mermaid.registerLayoutLoaders(elk);
              // securityLevel: "loose" — all diagram source here is generated by us
              // (Lean) and mmClean-sanitized, never user input, so we accept HTML in
              // node labels: the `<br/>` line breaks in #mermaid_explode summaries and
              // the hidden `<span class="lean-explode-full">…</span>` judgment marker
              // that diagram-hover.js surfaces on hover. In "strict" mode DOMPurify
              // would strip/escape these. DOMPurify still runs (loose ≠ no sanitize).
              mermaid.initialize({
                startOnLoad: false,
                theme: "redux-color",
                securityLevel: "loose",
              });
              mermaidApi = mermaid;
            })
            .catch(function (e) {
              console.error("mermaid/ELK ESM load failed", e);
            })
            .then(flush, flush);
        },
        render: render,
      };
    })(),

    mathjax: cdnPlugin({
      src: "https://cdn.jsdelivr.net/npm/mathjax@4/tex-mml-chtml.js",
      // 設定はバンドル読込み「前」に window.MathJax へ置く（onload では手遅れで
      // 既定設定のままページ全体が typeset される）。KaTeX との競合回避が要点:
      //   - startup.typeset=false  … ページ全体の自動 typeset を停止。MathJax が
      //       触れるのは render() が渡す .mathjax-block のみ（KaTeX 描画済みの
      //       $…$ / $$…$$ には一切触れない）。
      //   - インライン区切り $…$ は既定で無効のまま（KaTeX と衝突する唯一の
      //       区切りなので、あえて有効化しない）。
      // ブロック内の数式は $$…$$ もしくは \(…\) / \[…\] で書く。
      configure: function () {
        if (!window.MathJax) {
          window.MathJax = {
            startup: { typeset: false },
            tex: {
              inlineMath: [
                ["$", "$"],
                ["\\(", "\\)"],
              ],
              displayMath: [
                ["$$", "$$"],
                ["\\[", "\\]"],
              ],
            },
            options: { renderActions: { addMenu: [] } },
          };
        }
      },
      render: function (el, content) {
        var div = document.createElement("div");
        div.className = "mathjax-block";
        div.innerHTML = content;
        el.parentNode.replaceChild(div, el);
        if (window.MathJax && window.MathJax.typesetPromise) {
          window.MathJax.typesetPromise([div]);
        }
      },
    }),

    // rawsvg: 事前レンダリング済み SVG(例: Illuminate 出力)を literate に
    // 直接挿入する。CDN 不要。挿入後に diagram-hover.js でラベルを橋渡しする。
    // literate は core ブロックしか描画しないため、生 SVG は Block.code の
    // %%rawsvg ペイロードとして運び、ここで innerHTML 注入する。
    rawsvg: {
      load: function (cb) {
        cb();
      },
      render: function (el, content) {
        var wrap = document.createElement("div");
        wrap.className = "rawsvg-block";
        wrap.innerHTML = content;
        el.parentNode.replaceChild(wrap, el);
        if (window.LeanDiagramHover) window.LeanDiagramHover.enhance(wrap);
        if (window.DiagramView) window.DiagramView.attach(wrap);
      },
    },

    // ── 新しいプラグインの追加例 ──
    // tikz: cdnPlugin({
    //   src: "https://cdn.jsdelivr.net/npm/@nicolo-ribaudo/tikzjax@latest/dist/tikzjax.js",
    //   init: function () {},
    //   render: function (el, content) {
    //     var script = document.createElement("script");
    //     script.type = "text/tikz";
    //     script.textContent = content;
    //     el.parentNode.replaceChild(script, el);
    //   },
    // }),
  };

  // ── マーカー検出・処理 ──────────────────────────────────
  function processOne(pre) {
    if (pre.dataset.pluginDone) return;
    var text = pre.textContent;
    // O(1) ガード: 先頭 2 文字が %% (0x25 0x25) でなければ即脱出
    if (!text || text.charCodeAt(0) !== 37 || text.charCodeAt(1) !== 37) return;

    var nl = text.indexOf("\n");
    if (nl < 3) return;
    var name = text.substring(2, nl);
    var plug = plugins[name];
    if (!plug) return;

    pre.dataset.pluginDone = "1";
    pre.classList.add("plugin-loading");
    var content = text.substring(nl + 1);
    plug.load(function () {
      pre.classList.remove("plugin-loading");
      plug.render(pre, content);
    });
  }

  function scan(root) {
    // root 自身が <pre> の場合も処理する（Observer が <pre> を直接渡す経路に対応）。
    if (root.matches && root.matches("pre:not([data-plugin-done])"))
      processOne(root);
    var pres = root.querySelectorAll
      ? root.querySelectorAll("pre:not([data-plugin-done])")
      : [];
    for (var i = 0; i < pres.length; i++) processOne(pres[i]);
  }

  // 初期描画
  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", function () {
      scan(document);
    });
  } else {
    scan(document);
  }

  // 動的挿入対応（tippy ツールチップ等）
  if (typeof MutationObserver !== "undefined") {
    new MutationObserver(function (muts) {
      for (var i = 0; i < muts.length; i++) {
        var added = muts[i].addedNodes;
        for (var j = 0; j < added.length; j++) {
          if (added[j].nodeType === 1) scan(added[j]);
        }
      }
    }).observe(document.documentElement, { childList: true, subtree: true });
  }
})();
